# Adding a string that already has quotation marks
fact = "One of Northern Ireland's roads used to be called, \" Chuck Norris\"; but it had to be renamed because nobody crosses Chuck Norris and lives"
print(fact)

#Making a new line
act = "One of Northern Ireland's roads used to be called, \n\" Chuck Norris\"; but it had to be renamed because nobody crosses Chuck Norris and lives"
print(act)

#Tabbing something
ct = "One of Northern Ireland's roads used to be called, \t\" Chuck Norris\"; but it had to be renamed because nobody crosses Chuck Norris and lives"
print(ct)