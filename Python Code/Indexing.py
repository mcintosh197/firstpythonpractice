#Finding a single letter in a index
message = "Hello Python"
print(message[3])

#Finding the last number
message = "Hello Python"
print(message[-1])

#Find a range
message = "Hello Python"
print(message[0:5])

#This will print everything but the last letter
message = "Hello Python"
print(message[0:-1])

#Just take the end by default
message = "Hello Python"
print(message[0:])

#Just take the start by default
message = "Hello Python"
print(message[0:])