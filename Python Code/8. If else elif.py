x = input("name: ")

if x == "Kyle":
    print("You're great!")
# Elif must come after an if and before and else.
# Only one if and one else but can do many elif's.
elif x == "Drew":
    print("Thanks Drew")
elif x == "Corina":
    print ("Thanks Corina")
# Else must come after an if statements
# Don't need else statement after elif
else:
    print ("You're not great")

