x = "hello"
y = "hello"

print (x == y) # Are these things the same?
print (x != y) # Are these things not the same?

# == Checking for equality
# != Checking for inequality
# >= Greater than or equal to.
# <= Less than or equal to.
# > Greater than
# < Less than

